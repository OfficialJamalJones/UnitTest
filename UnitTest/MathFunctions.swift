import Foundation

public class MathFunctions {
    
    static func addNumbers(x: Int, y: Int) -> Int {
        return x + y
    }
    
    static func multiplyNumbers(x: Int, y: Int) -> Int {
        return x * y
    }
    
    static func divideNumbers(x: Int, y: Int) -> Int {
        return x / y
    }
    
}
